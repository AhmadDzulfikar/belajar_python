# operasi aritmatika

a = 3300
b = 3600

# Operasi tambah +
hasil = a + b
print(a, "+", b, "=", hasil)

# Operasi pengurangan -
hasil = a - b
print(a, "-", b, "=", hasil)

# Operasi perkalian *
hasil = a * b
print(a, "*", b, "=", hasil)

# Operasi perkalian /
hasil = a / b
print(a, "/", b, "=", hasil)

# Operasi eksponen (pangkat) **
# hasil = a ** b
# print(a, "pangkat", b, "=", hasil)

# Operasi modulus (sisa pembagian) %
hasil = a % b
print(a, "modulus (%)", b, "=", hasil)

# Operasi floor devision //
hasil = a // b
print(a, "//", b, "=", hasil)

'''
    Prioritas operasi
    ()
    eksponen **
    perkalian dan teman-teman * / % //
    pertambahan dan teman-teman + -
'''
x = 2
y = 5
z = 3

hasil = z * y ** x
print(z, 'x', y, '**', x, 'hasil =', hasil)

