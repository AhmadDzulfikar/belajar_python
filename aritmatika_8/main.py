# operasi aritmatika

a = 11
b = 5

# Operasi tambah +
hasil = a + b 
print(a, "+", b, "=", hasil)

# Operasi pengurangan -
hasil = a - b 
print(a, "-", b, "=", hasil)

# Operasi perkalian *
hasil = a * b 
print(a, "*", b, "=", hasil)

# Operasi perkalian /
hasil = a / b 
print(a, "/", b, "=", hasil)

# Operasi eksponen (pangkat) **
hasil = a ** b
print(a, "pangkat", b, "=", hasil)

# Operasi modulus (sisa pembagian) %
hasil = a % b
print(a, "modulus", b, "=", hasil)

# Operasi floor devision //
hasil = a // b
print(a, "//", b, "=", hasil)