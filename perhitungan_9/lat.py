# Ubah fahrenheit ke kelvin

fahrenheit = float(input("Masukkan angka fahrenheit kesini ="))

kelvin = 5/9 * (fahrenheit - 32) + 273
print("suhu", fahrenheit, "diubah ke kelvin adalah", kelvin, "kelvin")


